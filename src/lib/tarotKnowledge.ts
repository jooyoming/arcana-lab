// ══════════════════════════════════════════════════════════════════════════════
//  내부 유틸 — 비법서 데이터가 실제로 입력되었는지 체크
//  (빈 템플릿이면 AI 프롬프트에 포함하지 않아 토큰 낭비를 방지)
// ══════════════════════════════════════════════════════════════════════════════

const PLACEHOLDER_MARKERS = [
  '[지팡이 Ace (Ace of Wands)]\n\n[지팡이 2',   // 빈 템플릿 패턴
  '[0. 바보 (The Fool)]\n\n[1. 마법사',          // 빈 템플릿 패턴
];

/**
 * 비법서에 실제 데이터가 있으면 true를 반환합니다.
 * 빈 템플릿만 있을 경우 false를 반환하여 불필요한 토큰 소모를 막습니다.
 */
export function hasKnowledgeData(): boolean {
  const trimmed = TAROT_SECRET_BOOK.trim();
  if (trimmed.length < 200) return false;
  // 빈 템플릿 패턴이 포함되어 있으면 데이터 없음으로 간주
  return !PLACEHOLDER_MARKERS.some((marker) => trimmed.includes(marker));
}

// ── 비법서에서 카드 섹션을 파싱하는 유틸 ──────────────────────────────────────

/**
 * 비법서 텍스트를 섹션 단위([ ... ]로 시작하는 블록)로 분리하여 반환합니다.
 * 각 섹션은 { header, body } 형태입니다.
 */
function parseSections(): Array<{ header: string; body: string }> {
  const lines = TAROT_SECRET_BOOK.split('\n');
  const sections: Array<{ header: string; body: string }> = [];
  let currentHeader = '';
  let currentLines: string[] = [];

  for (const line of lines) {
    const trimmed = line.trim();
    // "[카드명]" 형식의 헤더 감지
    if (trimmed.startsWith('[') && trimmed.endsWith(']') && trimmed.length > 2) {
      // 이전 섹션 저장
      if (currentHeader) {
        sections.push({ header: currentHeader, body: currentLines.join('\n') });
      }
      currentHeader = trimmed.slice(1, -1); // 대괄호 제거
      currentLines = [];
    } else if (currentHeader) {
      currentLines.push(line);
    }
  }
  // 마지막 섹션 저장
  if (currentHeader) {
    sections.push({ header: currentHeader, body: currentLines.join('\n') });
  }

  return sections;
}

/**
 * 카드 이름 배열을 받아 비법서에서 해당 카드 섹션만 추출하여 문자열로 반환합니다.
 * AI 프롬프트에 필요한 섹션만 주입함으로써 토큰을 절감합니다.
 *
 * 매칭 전략:
 *  1. 카드 이름에서 괄호 안 영문명 추출 (예: "The Fool", "King of Wands")
 *  2. 해당 영문명이 섹션 헤더에 포함되는지 확인 (가장 정확)
 *  3. 없으면 한글 카드명 키워드로 폴백 매칭
 */
export function getCardsKnowledge(cardNames: string[]): string {
  if (!hasKnowledgeData() || cardNames.length === 0) return '';

  const sections = parseSections();
  const matched: string[] = [];

  for (const cardName of cardNames) {
    // 괄호 안 영문명 추출: "0 - 바보 (The Fool)" → "The Fool"
    //                        "지팡이 (Wands) - King (King of Wands)" → "King of Wands" (마지막 괄호)
    const englishMatches = cardName.match(/\(([^)]+)\)/g) || [];
    // 마지막 괄호(가장 구체적인 영문명)를 우선 사용
    const exactEnglish = englishMatches.length > 0
      ? englishMatches[englishMatches.length - 1].replace(/[()]/g, '').toLowerCase().trim()
      : '';

    // 한글 키워드 추출: 숫자/영어/괄호/하이픈을 제외한 한글 단어
    const koreanKeywords = cardName
      .replace(/[0-9a-zA-Z\(\)\-\.\s]/g, ' ')
      .split(/\s+/)
      .filter(w => w.length > 1);

    let bestMatch: typeof sections[0] | undefined;

    // 1순위: 영문 정식 카드명으로 정확 매칭
    if (exactEnglish) {
      bestMatch = sections.find(s => {
        const h = s.header.toLowerCase().replace(/[()]/g, '').trim();
        return h.includes(exactEnglish);
      });
    }

    // 2순위: 한글 키워드로 폴백 (단, 2글자 이상의 구체적 키워드만 사용)
    if (!bestMatch && koreanKeywords.length > 0) {
      // 가장 긴 한글 키워드부터 매칭 시도 (정확도 향상)
      const sortedKorean = [...koreanKeywords].sort((a, b) => b.length - a.length);
      for (const kw of sortedKorean) {
        bestMatch = sections.find(s => s.header.includes(kw));
        if (bestMatch) break;
      }
    }

    if (bestMatch) {
      const sectionText = `[${bestMatch.header}]\n${bestMatch.body.trim()}`;
      if (!matched.includes(sectionText)) {
        matched.push(sectionText);
      }
    }
  }

  return matched.join('\n\n');
}

/**
 * 비법서에 있는 총 카드 섹션 수를 반환합니다.
 * 콘솔 로그용 디버그 정보에 사용됩니다.
 */
export function getKnowledgeSectionCount(): number {
  if (!hasKnowledgeData()) return 0;
  return parseSections().length;
}
